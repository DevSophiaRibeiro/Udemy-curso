let salario = 500
let bonus = 100

salario += bonus // salario = salario + bonus
console.log(salario) // 600

salario -= bonus // salario = salario - bonus
console.log(salario) // 500  

salario *= 2 // salario = salario * 2
console.log(salario) // 1000

salario /= 4 // salario = salario / 4
console.log(salario) // 250

